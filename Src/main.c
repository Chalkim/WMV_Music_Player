/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_SPI2_Init();
  MX_USB_DEVICE_Init();
  MX_SPI3_Init();
  MX_FATFS_Init();
  MX_TIM6_Init();
  MX_DAC_Init();
  /* USER CODE BEGIN 2 */
	LED0_ON;
	
  if(SD_mount() == FR_OK)    	
	{
		scan_files ("");
		UI_Init();
		if (file_num == 0)   UI_Info("NO WAV",RED);
  }else
	{
		UI_Init();
	  UI_Info("SD Error",RED);
	}
//	UI_Info("I'm Colorful",RED);
	
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
		if(state == 1)                                 {WAV_CHBUF(&WAV_file,sdBUF,DMA_BUF,PCMBit,WAY);}
		//开发板按键
		if (KEY0_StateRead() == KEY_DOWN)              {LEDx_StateSet(LED0,LED_TOGGLE);}
		if (KEY1_StateRead() == KEY_DOWN)              {LEDx_StateSet(LED1,LED_TOGGLE);}
		//用户按键
		if(KEY_UP_StateRead() == KEY_DOWN)             
		{
			select--;
			if(select == 0)select = 1; 
      UI_Select(select);
		}
		if(KEY_DOWN_StateRead() == KEY_DOWN)           
		{
      select++;
		  if(select > file_num) select = file_num;
			UI_Select(select);
		}
		if(KEY_LEFT_StateRead() == KEY_DOWN)           
		{
			u16 ii;
			HAL_TIM_Base_Stop(&htim6);
			HAL_DAC_Stop_DMA(&hdac,DAC_CHANNEL_1);
			for(ii=0;ii<=DMA_BUF_Size;ii++)
			{			
				DMA_BUF[ii]=0;
			}
			state = 0;
		}
		if(KEY_RIGHT_StateRead() == KEY_DOWN)         
    {
			HAL_TIM_Base_Stop(&htim6);
			HAL_DAC_Stop_DMA(&hdac,DAC_CHANNEL_1);
			WAV_STOP(&WAV_file);
			state = 2;
		}
		if(KEY_OK_StateRead() == KEY_DOWN)            
    {
			if (select - 1 != old || state == 2)
			{
				HAL_TIM_Base_Stop(&htim6);
			  HAL_DAC_Stop_DMA(&hdac,DAC_CHANNEL_1);
			  WAV_STOP(&WAV_file);
			  state = 0;
				
				WAV_GET_Info(names[select - 1],&WAVInfo);
				WAV_GET_SAMRate(&WAVInfo,&SAMRate);
				WAV_GET_PCMBit(&WAVInfo,&PCMBit);
				WAV_GET_WAY(&WAVInfo,&WAY);
				WAV_PreLoad(names[select - 1],&WAV_file);
				WAV_READ(&WAV_file,sdBUF,DMA_BUF,0, PCMBit, WAY);
				WAV_READ(&WAV_file,sdBUF,DMA_BUF,1, PCMBit, WAY);
				MX_TIM6_Init();
				old = select - 1;
				
				HAL_TIM_Base_Start(&htim6);
				HAL_DAC_Start_DMA(&hdac,DAC_CHANNEL_1,(uint32_t *)DMA_BUF,DMA_BUF_Size,DAC_ALIGN_12B_L);
				state = 1;
			}else
			{
				HAL_TIM_Base_Start(&htim6);
				HAL_DAC_Start_DMA(&hdac,DAC_CHANNEL_1,(uint32_t *)DMA_BUF,DMA_BUF_Size,DAC_ALIGN_12B_L);
				state = 1;
			}
			

		}
  }
  /* USER CODE END 3 */
	
}

